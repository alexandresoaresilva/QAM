    wait until rising_edge(aclk);
    wait for T_HOLD;

    -- Program the core with the initial configuration.
    -- The phase increment and phase offset values here are derived from the output frequency and
    -- phase offset angle values set in the CORE Generator GUI.
    -- The config slave channel packet is one transaction per TDM channel
    for cfg_channel in 0 to 12 loop
      s_axis_config_tvalid <= '1';
      s_axis_config_tdata <= (others => '0');  -- set unused TDATA bits to zero
      case cfg_channel is
        when 0 =>
          s_axis_config_tdata(24 downto 0) <= "0000000011010100111111011";  -- channel 0 phase increment
          s_axis_config_tdata(56 downto 32) <= "1000110100011011011100011";  -- channel 0 phase offset
        when 1 =>
          s_axis_config_tdata(24 downto 0) <= "0000000011010100111111011";  -- channel 1 phase increment
          s_axis_config_tdata(56 downto 32) <= "1010000000000000000000000";  -- channel 1 phase offset
        when 2 =>
          s_axis_config_tdata(24 downto 0) <= "0000000011010100111111011";  -- channel 2 phase increment
          s_axis_config_tdata(56 downto 32) <= "1011001011100100100011110";  -- channel 2 phase offset
