      ,s_axis_config_tvalid            => s_axis_config_tvalid
      ,s_axis_config_tdata             => s_axis_config_tdata
      ,s_axis_config_tlast             => s_axis_config_tlast
      ,m_axis_data_tvalid              => m_axis_data_tvalid
