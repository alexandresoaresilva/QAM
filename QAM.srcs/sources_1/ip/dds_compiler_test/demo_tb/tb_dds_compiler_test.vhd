          s_axis_config_tdata(56 downto 32) <= "0000000000000000000000000";  -- channel 12 phase offset
      end case;
      if cfg_channel = 12 then
        s_axis_config_tlast <= '1';
      else
        s_axis_config_tlast <= '0';
      end if;
      wait for CLOCK_PERIOD;
    end loop;
